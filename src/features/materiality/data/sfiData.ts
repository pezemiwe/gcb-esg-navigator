const calculateKPIs = () => {
  const totalVolume = SFI_DATA.reduce(
    (sum, deal) => sum + deal.Deal_Amount_USD,
    0,
  );
  const totalOutstanding = SFI_DATA.reduce(
    (sum, deal) => sum + deal.Outstanding_Amount,
    0,
  );

  const regionBreakdown = SFI_DATA.reduce(
    (acc, deal) => {
      acc[deal.Region] = (acc[deal.Region] || 0) + deal.Deal_Amount_USD;
      return acc;
    },
    {} as Record<string, number>,
  );

  const esgCategoryBreakdown = SFI_DATA.reduce(
    (acc, deal) => {
      acc[deal.ESG_Category] =
        (acc[deal.ESG_Category] || 0) + deal.Deal_Amount_USD;
      return acc;
    },
    {} as Record<string, number>,
  );

  const sectorBreakdown = SFI_DATA.reduce(
    (acc, deal) => {
      acc[deal.Sector] = (acc[deal.Sector] || 0) + deal.Deal_Amount_USD;
      return acc;
    },
    {} as Record<string, number>,
  );

  const maturityBreakdown = SFI_DATA.reduce(
    (acc, deal) => {
      acc[deal.Maturity] = (acc[deal.Maturity] || 0) + 1;
      return acc;
    },
    {} as Record<string, number>,
  );

  const monitoringStatusBreakdown = SFI_DATA.reduce(
    (acc, deal) => {
      acc[deal.Monitoring_Status] = (acc[deal.Monitoring_Status] || 0) + 1;
      return acc;
    },
    {} as Record<string, number>,
  );

  const riskBreakdown = SFI_DATA.reduce(
    (acc, deal) => {
      acc[deal.Risk] = (acc[deal.Risk] || 0) + 1;
      return acc;
    },
    {} as Record<string, number>,
  );

  const financingTypeBreakdown = SFI_DATA.reduce(
    (acc, deal) => {
      acc[deal.Financing_Type] =
        (acc[deal.Financing_Type] || 0) + deal.Deal_Amount_USD;
      return acc;
    },
    {} as Record<string, number>,
  );

  const useOfProceedsBreakdown = SFI_DATA.reduce(
    (acc, deal) => {
      acc[deal.Use_of_Proceeds] =
        (acc[deal.Use_of_Proceeds] || 0) + deal.Deal_Amount_USD;
      return acc;
    },
    {} as Record<string, number>,
  );

  const verificationBreakdown = SFI_DATA.reduce(
    (acc, deal) => {
      acc[deal.External_Verification_Status] =
        (acc[deal.External_Verification_Status] || 0) + 1;
      return acc;
    },
    {} as Record<string, number>,
  );

  const riskCountByClientBreakdown = SFI_DATA.reduce(
    (acc, deal) => {
      const key = deal.Risk;
      if (!acc[key]) acc[key] = new Set<string>();
      acc[key].add(deal.Client_Name);
      return acc;
    },
    {} as Record<string, Set<string>>,
  );
  const riskClientCount = Object.fromEntries(
    Object.entries(riskCountByClientBreakdown).map(([k, v]) => [k, v.size]),
  );

  return {
    dealCount: SFI_DATA.length,
    totalVolume,
    totalOutstanding,
    regionBreakdown,
    esgCategoryBreakdown,
    sectorBreakdown,
    maturityBreakdown,
    monitoringStatusBreakdown,
    riskBreakdown,
    financingTypeBreakdown,
    useOfProceedsBreakdown,
    verificationBreakdown,
    riskClientCount,
    totalCO2Avoided: SFI_DATA.reduce(
      (sum, deal) => sum + (deal.tCO2e_Avoided || 0),
      0,
    ),
    totalJobsCreated: SFI_DATA.reduce(
      (sum, deal) => sum + (deal.Jobs_Created || 0),
      0,
    ),
    totalSMEsSupported: SFI_DATA.reduce(
      (sum, deal) => sum + (deal.SMEs_Supported || 0),
      0,
    ),
    totalRenewableMW: SFI_DATA.reduce(
      (sum, deal) => sum + (deal.Renewable_MW_Installed || 0),
      0,
    ),
    countryCount: new Set(SFI_DATA.map((deal) => deal.Country)).size,
  };
};

